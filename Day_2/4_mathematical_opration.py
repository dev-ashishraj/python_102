print(123 + 234) # Addition
print(123 - 234) # Subtraction
print(123 * 234) # Multiplication
print(123 % 234) # Modulo (This finds the remainder)
print(123 / 234) # Division (Returns a float)
print(123 // 234) # Floor Division (Removes decimal, returns whole number)
print(123 ** 2)  # Exponentiation (123 to the power of 2)

# PEMDAS Logic
print(3*3+3/3-3)    # Result is 7.0
print(3*(3+3)/3-3)  # Result is 3.0